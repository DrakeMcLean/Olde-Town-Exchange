  <h1 class='teal' style='text-align: center;'><b>How To Consign</b></h1>

  <h1 class='brown'><b>Consignment By Appointment</b></h1>
  <h3 class='brown'>
    Please call the store to set up your time. Appointments will be every half hour.  We are looking for clothing that is in excellent condition and current style.  Clothes, bags, jewelry and shoes are our main best selling items.
    <br><br>
    Consider and re-look at what you're bringing in. It has to be CLEAN! With the Coronavirus present, we will not be taking unwashed/dirty items. If it gets in the back by mistake, we will be discarding it. No one wants to get sick, so please, please be mindful of what you're asking us to sell.
  </h3>

  <br>

  <h3 class='brown'>
    <b>Brand Names:</b>
    <br> We are looking for higher-end and specialized items.  This allows us to create a unique shopping experience for our customers.  If we accidentally accept an item that is not an upscale name (check our list), we will donate it.  We donate clothes to a women's charity that helps them get back on their feet.
  </h3>

  <br>

  <h3 class='brown'>
    <b>Clean:</b>
    <br> We ask that you check your clothes for stains, holes, snags, missing buttons, broken zippers, ripped seams, etc. before you bring them into the store.  If it's not in excellent condition, we can't sell it.
  </h3>

  <br>

  <h3 class='brown'>
    <b>Current Style:</b>
    <br>We are looking for current style, unique or &quot;one of a kind&quot; items.  Items should be recent, within the past 5-years.  We specialize in Boho Chic, Business Casual, Streetstyle and Athletic Wear.  Accessories range from Purses to Bags to Belts to Hats.  Jewelry is always looked at, from Sterling to Native American to Fashion pieces.
  </h3>

  <br>
    
  <h1 class='brown'><b>Financial Agreement</b></h1>
  <h3 class='brown'>
    We offer our consignors a 50/50 split or a 60/40 split depending on whether you use your earnings as store credit or take a check. We do not charge extra fees.  As soon as an item sells, the earnings go into your consignor account.  You may use your earnings as store credit or request a check.  If, for some reason, it takes us longer than 30-minutes to prepare an item for sale, the split will be 70/30 on that item regardless of the payout method.
  </h3>

  <h1 class='teal' style='text-align: center;'>We look forward to seeing you!</h1>

